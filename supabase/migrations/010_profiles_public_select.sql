-- profilesテーブルに認証済みユーザー間のSELECTポリシーを追加
-- コーチ回答のJOINでコーチのプロフィール情報を取得するために必要
-- =============================================

-- 認証済みユーザーは他ユーザーの公開プロフィールを閲覧可能
CREATE POLICY "プロフィール: 認証済みユーザーは閲覧可能"
  ON profiles FOR SELECT
  USING (auth.role() = 'authenticated');
